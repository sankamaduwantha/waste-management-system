const User = require('../models/User');
const Resident = require('../models/Resident');
const crypto = require('crypto');
const { sendEmail } = require('../utils/emailService');

// @desc    Register user
// @route   POST /api/v1/auth/register
// @access  Public
exports.register = async (req, res, next) => {
  try {
    const { name, email, password, role, phone, address, zone } = req.body;

    console.log('Registration attempt:', { name, email, role, phone, hasPassword: !!password });

    // Check if user already exists
    const existingUser = await User.findOne({ email });
    if (existingUser) {
      return res.status(400).json({
        status: 'error',
        message: 'User already exists with this email'
      });
    }

    // Create user
    const user = await User.create({
      name,
      email,
      password,
      role: role || 'resident',
      phone,
      address,
      zone
    });

    // If resident, create resident profile
    if (user.role === 'resident') {
      await Resident.create({
        user: user._id
      });
    }

    // Send welcome email
    await sendEmail({
      to: user.email,
      subject: 'Welcome to Waste Management System',
      text: `Hi ${user.name},\n\nWelcome to our waste management platform!`
    }).catch(err => console.log('Email error:', err));

    sendTokenResponse(user, 201, res);
  } catch (error) {
    console.error('Registration error details:', error);
    next(error);
  }
};

// @desc    Login user
// @route   POST /api/v1/auth/login
// @access  Public
exports.login = async (req, res, next) => {
  try {
    const { email, password } = req.body;

    // Validate email & password
    if (!email || !password) {
      return res.status(400).json({
        status: 'error',
        message: 'Please provide email and password'
      });
    }

    // Check for user (include password)
    const user = await User.findOne({ email }).select('+password');

    if (!user) {
      return res.status(401).json({
        status: 'error',
        message: 'Invalid credentials'
      });
    }

    // Check if user is active
    if (user.status !== 'active') {
      return res.status(401).json({
        status: 'error',
        message: 'Your account has been deactivated. Please contact support.'
      });
    }

    // Check if password matches
    const isMatch = await user.matchPassword(password);

    if (!isMatch) {
      return res.status(401).json({
        status: 'error',
        message: 'Invalid credentials'
      });
    }

    // Update last login
    user.lastLogin = new Date();
    await user.save({ validateBeforeSave: false });

    sendTokenResponse(user, 200, res);
  } catch (error) {
    next(error);
  }
};

// @desc    Get current logged in user
// @route   GET /api/v1/auth/me
// @access  Private
exports.getMe = async (req, res, next) => {
  try {
    const user = await User.findById(req.user.id).populate('zone');
    
    // If resident, get resident profile
    let residentProfile = null;
    if (user.role === 'resident') {
      residentProfile = await Resident.findOne({ user: user._id })
        .populate('binAssigned')
        .populate('collectionSchedule');
    }

    res.status(200).json({
      status: 'success',
      data: {
        user,
        residentProfile
      }
    });
  } catch (error) {
    next(error);
  }
};

